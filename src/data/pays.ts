// Sort alphabetically by French name
PAYS.sort((a, b) => a.nom.localeCompare(b.nom, "fr"));

export function getPaysNom(code: string): string {
  return PAYS.find((p) => p.code === code)?.nom ?? code;
}

export function searchPays(query: string): Pays[] {
  const q = query.toLowerCase();
  return PAYS.filter(
    (p) => p.nom.toLowerCase().includes(q) || p.code.toLowerCase().includes(q)
  );
}
